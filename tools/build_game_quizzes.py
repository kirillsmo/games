INTRO = ('<p style="color:var(--muted);margin-top:-2px">Выбери ответ. Ошибёшься — '
         '30 секунд на то, чтобы вернуться в урок и перечитать.</p>')


def esc(s):
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def build_block(questions):
    out = ['  <div class="quiz" data-iter="2000" data-cooldown="30">',
           '    <h2>🧠 Проверь себя</h2>',
           '    ' + INTRO]
    for qid, prompt, options, explain in questions:
        h = quizhash(qid, options[0], ITER)
        opts = options[:]
        random.Random(qid).shuffle(opts)          # порядок в исходнике не выдаёт ответ
        out.append('')
        out.append('    <div class="quiz-q" data-id="%s" data-hash="%s">' % (qid, h))
        out.append('      <p class="quiz-prompt">%s</p>' % esc(prompt))
        out.append('      <div class="quiz-options">')
        for o in opts:
            out.append('        <button type="button" class="quiz-opt">%s</button>' % esc(o))
        out.append('      </div>')
        out.append('      <div class="quiz-explain">✅ %s</div>' % explain)
        out.append('    </div>')
    out.append('  </div>')
    return "\n".join(out)


def main():
    changed = 0
    for rel, questions in Q.items():
        path = os.path.join(ROOT, rel)
        with open(path, encoding="utf-8") as fh:
            html = fh.read()
        if 'class="quiz"' in html:
            print("skip (уже есть):", rel)
            continue
        block = build_block(questions)
        marker = '  <div class="pager">'
        if marker not in html:
            print("!! нет pager:", rel); continue
        html = html.replace(marker, block + "\n\n" + marker, 1)
        if "quiz.js" not in html:
            html = html.replace("</body>", '<script src="../../quiz.js"></script>\n</body>', 1)
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(html)
        changed += 1
        print("ok:", rel, "(%d вопроса)" % len(questions))
    print("\nИзменено файлов:", changed)


if __name__ == "__main__":
    main()
